pub fn aritmeticos() {
    let x = 10;
    let y = 5;

    println!("x + y = {}", x + y);
    println!("x - y = {}", x + y);
    println!("x * y = {}", x + y);
    println!("x / y = {}", x + y);
    println!("x % y = {}", x + y);
}

pub fn relacionis() {
    let x = 10;
    let y = 5;

    println!("x == y = {}", x == y);
    println!("x != y = {}", x != y);
    println!("x > y = {}", x > y);
    println!("x < y = {}", x < y);
    println!("x >= y = {}", x >= y);
    println!("x <= y = {}", x <= y);
}
